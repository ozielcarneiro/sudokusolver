    private void casa1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_casa1MouseClicked
        // TODO add your handling code here:
        if (select[0][0] == false) {
            if (selrow < 9 && selcol < 9) {
                select[selrow][selcol] = false;
                casas[selrow][selcol].setForeground(Color.black);
            }
            casas[0][0].setForeground(Color.blue);
            jPanel4.grabFocus();
            select[0][0] = true;
            selrow = 0;
            selcol = 0;
        } else {
            casas[0][0].setForeground(Color.black);
            select[0][0] = false;
            selrow = 9;
            selcol = 9;
            jButton1.grabFocus();
        }
    }//GEN-LAST:event_casa1MouseClicked

